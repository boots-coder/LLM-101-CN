/**
 * generate-content-map.js
 *
 * 从 docs/ 下所有 .md 文件自动生成：
 * 1. 项目根目录的 CONTENT_MAP.md（全局索引）
 * 2. 每个模块 index.md 中的详细内容索引区块
 *
 * 用法：node scripts/generate-content-map.js
 * 或：  npm run map
 */

import { readFileSync, writeFileSync, readdirSync, statSync, existsSync, mkdirSync } from 'fs'
import { join, relative, dirname, basename } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const DOCS = join(ROOT, 'docs')
const AUTO_START = '<!-- AUTO-GENERATED-CONTENT-INDEX-START -->'
const AUTO_END = '<!-- AUTO-GENERATED-CONTENT-INDEX-END -->'

// 模块顺序（与 config.ts sidebar 一致）
const MODULE_ORDER = [
  'fundamentals', 'architecture', 'training', 'engineering',
  'applications', 'deep-dives', 'exercises'
]

const MODULE_NAMES = {
  fundamentals: '基础知识',
  architecture: '模型架构',
  training: '训练',
  engineering: '工程化',
  applications: '应用',
  'deep-dives': '深度剖析',
  exercises: '练习系统',
}

// ── 解析单个 .md 文件 ──────────────────────────────────────

function parseFile(filePath) {
  const content = readFileSync(filePath, 'utf-8')
  const lines = content.split('\n')
  const totalLines = lines.length

  // 解析 YAML frontmatter
  let frontmatter = {}
  let bodyStartLine = 0
  if (lines[0] === '---') {
    const endIdx = lines.indexOf('---', 1)
    if (endIdx > 0) {
      bodyStartLine = endIdx + 1
      const yamlBlock = lines.slice(1, endIdx).join('\n')
      frontmatter = parseSimpleYaml(yamlBlock)
    }
  }

  // 提取 # 标题（如果 frontmatter 没有 title）
  if (!frontmatter.title) {
    for (let i = bodyStartLine; i < lines.length; i++) {
      if (lines[i].startsWith('# ')) {
        frontmatter.title = lines[i].slice(2).trim()
        break
      }
    }
  }

  // 提取所有 ## 和 ### 标题及行号
  const headings = []
  for (let i = 0; i < lines.length; i++) {
    if (lines[i].startsWith('### ')) {
      headings.push({
        level: 3,
        title: lines[i].slice(4).trim(),
        line: i + 1, // 1-based
      })
    } else if (lines[i].startsWith('## ')) {
      headings.push({
        level: 2,
        title: lines[i].slice(3).trim(),
        line: i + 1, // 1-based
      })
    }
  }

  return {
    path: filePath,
    relativePath: relative(DOCS, filePath),
    totalLines,
    frontmatter,
    headings,
  }
}

// 简易 YAML 解析（支持 title, description, topics, prereqs）
function parseSimpleYaml(yaml) {
  const result = {}
  for (const line of yaml.split('\n')) {
    const match = line.match(/^(\w+):\s*(.+)$/)
    if (match) {
      const [, key, value] = match
      if (value.startsWith('[') && value.endsWith(']')) {
        // 解析数组
        result[key] = value.slice(1, -1).split(',').map(s => s.trim().replace(/^["']|["']$/g, ''))
      } else {
        result[key] = value.replace(/^["']|["']$/g, '')
      }
    }
  }
  return result
}

// ── 收集所有文件并按模块分组 ────────────────────────────────

function collectFiles() {
  const modules = {}

  for (const mod of MODULE_ORDER) {
    const modDir = join(DOCS, mod)
    if (!existsSync(modDir)) continue

    const files = readdirSync(modDir)
      .filter(f => f.endsWith('.md') && f !== 'index.md')
      .sort()

    modules[mod] = files.map(f => parseFile(join(modDir, f)))
  }

  return modules
}

// ── 生成 CONTENT_MAP.md ────────────────────────────────────

function generateContentMap(modules) {
  const lines = []
  const now = new Date().toISOString().slice(0, 10)

  lines.push('# Content Map')
  lines.push(`<!-- AUTO-GENERATED by scripts/generate-content-map.js — do not edit manually -->`)
  lines.push(`<!-- Last generated: ${now} -->`)
  lines.push('')
  lines.push('> LLM 搜索入口：先在此文件定位目标文件和行号，再用 `Read file offset=<行号>` 精准跳转。')
  lines.push('')

  // 总览
  lines.push('## 项目总览')
  lines.push('')
  let totalFiles = 0
  let totalLines = 0
  for (const [mod, files] of Object.entries(modules)) {
    const modLines = files.reduce((sum, f) => sum + f.totalLines, 0)
    totalFiles += files.length
    totalLines += modLines
    lines.push(`- **${mod}/** (${MODULE_NAMES[mod]}) — ${files.length} files, ~${modLines} lines`)
  }
  lines.push('')
  lines.push(`**Total: ${totalFiles} content files, ~${totalLines} lines**`)
  lines.push('')
  lines.push('---')
  lines.push('')

  // 每个模块的详细索引
  for (const [mod, files] of Object.entries(modules)) {
    const modLines = files.reduce((sum, f) => sum + f.totalLines, 0)
    lines.push(`## ${mod}/ — ${MODULE_NAMES[mod]} (${files.length} files, ~${modLines} lines)`)
    lines.push('')

    for (const file of files) {
      const fm = file.frontmatter
      const title = fm.title || basename(file.relativePath, '.md')
      const desc = fm.description ? ` — ${fm.description}` : ''
      lines.push(`### ${basename(file.relativePath)}${desc} (${file.totalLines} lines)`)

      if (fm.topics && fm.topics.length > 0) {
        lines.push(`topics: ${fm.topics.join(', ')}`)
      }

      for (const h of file.headings) {
        const indent = h.level === 3 ? '  ' : ''
        lines.push(`${indent}- ${h.title} (L${h.line})`)
      }
      lines.push('')
    }

    lines.push('---')
    lines.push('')
  }

  return lines.join('\n')
}

// ── 更新模块 index.md 的自动生成区块 ───────────────────────

function updateModuleIndex(mod, files) {
  const indexPath = join(DOCS, mod, 'index.md')
  if (!existsSync(indexPath)) return

  let content = readFileSync(indexPath, 'utf-8')

  // 生成索引区块
  const indexLines = []
  indexLines.push('')
  indexLines.push('## 详细内容索引')
  indexLines.push(AUTO_START)
  indexLines.push('')

  for (const file of files) {
    const fm = file.frontmatter
    const title = fm.title || basename(file.relativePath, '.md')
    const fname = basename(file.relativePath)
    indexLines.push(`### ${fname} — ${title} (${file.totalLines} lines)`)

    for (const h of file.headings) {
      const indent = h.level === 3 ? '  ' : ''
      indexLines.push(`${indent}- ${h.title} (L${h.line})`)
    }
    indexLines.push('')
  }

  indexLines.push(AUTO_END)
  const newBlock = indexLines.join('\n')

  // 替换或追加
  if (content.includes(AUTO_START)) {
    const startIdx = content.indexOf(AUTO_START)
    const beforeStart = content.lastIndexOf('\n## 详细内容索引', startIdx)
    const endIdx = content.indexOf(AUTO_END)
    if (endIdx !== -1) {
      const cutStart = beforeStart !== -1 ? beforeStart : startIdx
      content = content.slice(0, cutStart) + newBlock + content.slice(endIdx + AUTO_END.length)
    }
  } else {
    // 追加到文件末尾
    content = content.trimEnd() + '\n' + newBlock + '\n'
  }

  writeFileSync(indexPath, content, 'utf-8')
  console.log(`  Updated: docs/${mod}/index.md`)
}

// ── Main ────────────────────────────────────────────────────

console.log('Generating content map...\n')

const modules = collectFiles()

// 1. 生成 CONTENT_MAP.md
const contentMap = generateContentMap(modules)
writeFileSync(join(ROOT, 'CONTENT_MAP.md'), contentMap, 'utf-8')
console.log('Generated: CONTENT_MAP.md')

// 2. 更新每个模块的 index.md
for (const [mod, files] of Object.entries(modules)) {
  updateModuleIndex(mod, files)
}

// 3. 生成 docs/public/content-index.json（供 AI 助教组件使用）
function generateContentIndex(modules) {
  const index = {}
  for (const [mod, files] of Object.entries(modules)) {
    for (const file of files) {
      const key = `${mod}/${basename(file.relativePath)}`
      const fm = file.frontmatter
      index[key] = {
        title: fm.title || basename(file.relativePath, '.md'),
        topics: fm.topics || [],
        sections: file.headings.map(h => ({ heading: h.title, level: h.level })),
      }
    }
  }
  return index
}

const publicDir = join(DOCS, 'public')
if (!existsSync(publicDir)) {
  mkdirSync(publicDir, { recursive: true })
}
const contentIndex = generateContentIndex(modules)
writeFileSync(join(publicDir, 'content-index.json'), JSON.stringify(contentIndex, null, 2), 'utf-8')
console.log('Generated: docs/public/content-index.json')

// 4. 统计
let totalFiles = 0
let totalHeadings = 0
for (const files of Object.values(modules)) {
  totalFiles += files.length
  for (const f of files) {
    totalHeadings += f.headings.length
  }
}
console.log(`\nDone! Indexed ${totalFiles} files with ${totalHeadings} headings.`)
